#Q.4)WAP to get area of circle.

# Prompt user to enter the radius of the circle
number1 = int(input("Enter the radius of the circle: "))

# Calculate the area of the circle using the formula: area = 3.14 * radius * radius
area = 3.14 * number1 * number1

# Display the calculated area to the user
print("The area of the circle is", area)
