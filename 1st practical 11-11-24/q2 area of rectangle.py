#Q.2)WAP to get area of rectangle.

# Prompt the user to enter the length and breadth of the rectangle
length = int(input("Enter the length of the rectangle: "))
breadth = int(input("Enter the breadth of the rectangle: "))

# Calculate the area of the rectangle using the formula: area = length * breadth
area = length * breadth

# Display the calculated area to the user
print("The area of the rectangle is", area)
